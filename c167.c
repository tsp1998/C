#include <stdio.h>
#include <stdlib.h>
void main()
{
  char ch;

  scanf("%c", &ch);
  switch (ch)
  {
  case 'a':
    printf("\n\ta");
    break;
  case 'b':
    printf("\n\tb");
    break;
  }
  main();
}